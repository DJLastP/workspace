//모든 소스코드는 반드시 클래스 안에 작성해야 한다.!
//모든 소스코드는 반드시 main 메소드 안에 작성해야 한다.!
//main 메소드 안의 소스코드는 위에서부터 한 줄씩
//차례대로 실행된다.
public class 출력물01 {
    public static void main(String[] args){
        //()안에 글자가 없으면 한 줄 개행만 실행.
        System.out.println();
        System.out.println("java");
        System.out.println("123");
        //숫자를 출력할때는 ""(쌍따옴표)를 사용하지 않는다
        System.out.println(123);
        //글자를 출력할때는 반드시 쌍따옴표에 감싸야 함
        //System.out.println(java);
        System.out.println(123.45);
        //사칙연산 가능한 실수는 ""없이 입력한다.
    }
}

