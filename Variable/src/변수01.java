//변수는 변하는 값을 저장하는 공간.
//변수를 사용하기 위해서는 반드시 변수를 선언해야함.
public class 변수01 {
    public static void main(String[] args){
        //변수 선언 문법 : 자료형 변수명;
        int a; //정수를 담을수있는 a라는 이름의 상자를 만듦.
        // =(대입연산자)
        // =를 기준으로 우측항을 좌측항에 대입한다(저장)
        a = 10;
        System.out.println(a);
        System.out.println("a");
    }

}
