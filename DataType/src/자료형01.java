public class 자료형01 {
    public static void main(String[] args) {
        //참, 거짓을 저장할 수 있는 자료형
        boolean bool = true; //false
        System.out.println("bool = " + bool);
        //정수를 저장할 수 있는 자료형
        //byte, short, int, long
        byte num1 = 10;
        //num1 = 128; 오류

        //실수를 저장할 수 있는 자료형
        //float, double
        double num2 = 10.5;
        System.out.println(num2); 
    }
}
